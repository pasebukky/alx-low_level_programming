#include "main.h"
#include <stdio.h>

/**
 * add - adds to integers
 * @a: first integer
 * @b: second integer
 * Return: Answer after addition
 *
 */

int add(int a, int b)
{
return (a + b);
}



/**
 * sub - subracts two integers
 * @a: first integer
 * @b: second integer
 * Return: Answer after subtraction
 */
int sub(int a, int b)
{
return (a - b);
}





/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: Answer after multiplication
 */
int mul(int a, int b)
{
return (a * b);
}





/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: Answer after division without remainder
 */
int div(int a, int b)
{
return (a / b);
}





/**
 * mod - modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: Answer after modulus operation 
 */
int mod(int a, int b)
{
return (a % b);
}

